# Lista numerica.
numeros = [5, 6, 8, 10, 15, 13]

# variaval que armazenaram o resultado das funções.
soma = sum(numeros)
maior = max(numeros)
menor = min(numeros)

# Exibe o resultado das funções.
print(f"A soma dos numeros é: {soma}")
print(f"O maior numero da sequencia é: {maior}")
print(f"O menor numero da sequencia é: {menor}")

# Numero negativo
negativo = -9293123

# Retorna o valor absoluto no numero.
absoluto =abs(negativo)

# Exibe o valor absoluto
print(f"O valor absoluto é: {absoluto}")

